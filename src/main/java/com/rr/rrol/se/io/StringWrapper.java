package com.rr.rrol.se.io;

public class StringWrapper {

	private int length;
	private String string;
	
	public StringWrapper(int length, String string) {
		super();
		this.length = length;
		this.string = string;
	}

	public int getLength() {
		return length;
	}

	public String getString() {
		return string;
	};
	
}
